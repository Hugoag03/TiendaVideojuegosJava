    private void BotonFinalizarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonFinalizarCompraActionPerformed
        // TODO add your handling code here:

        nombreComprador = NombreCompra.getText();
        apellidosComprador = ApellidosCompra.getText();
        dniComprador = DNICompra.getText();
        cuentaComprador = BancoCompra.getText();
        correoCompra = CorreoCompra.getText();

        if (CheckImp.isSelected()) {

            if (NombreCompra.getText().isEmpty() || DNICompra.getText().isEmpty() || ApellidosCompra.getText().isEmpty() || BancoCompra.getText().isEmpty() || CorreoCompra.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Debes rellenar todos los campos antes de completar la compra", "Escribe los datos", JOptionPane.WARNING_MESSAGE);
            } else {
                fecha = java.time.LocalDate.now();

                if (IniciarSesionCliente.cogerNombreUsu == true || RegistrarCliente.bandera == true) {
                    try {
                        Connection cn = conexion.Conexion.conectar();

                        String enviarConsulta = "INSERT INTO compras (NombreComprador, ApellidosComprador, DNIComprador, CuentaBancaria, CorreoCompra, ID_CLIENTES, ID_VIDEOJUEGOS) VALUES (?, ?, ?, ?, ?, ?, ?) ";

                        PreparedStatement sentencia = cn.prepareStatement(enviarConsulta);

                        sentencia.setString(1, nombreComprador);
                        sentencia.setString(2, apellidosComprador);
                        sentencia.setString(3, dniComprador);
                        sentencia.setString(4, cuentaComprador);
                        sentencia.setString(5, correoCompra);
                        if (RegistrarCliente.bandera == true) {
                            sentencia.setInt(6, RegistrarCliente.ID);
                        } else {
                            sentencia.setInt(6, IniciarSesionCliente.ID);
                        }
                        sentencia.setInt(7, Resultados.ID);

                        sentencia.executeUpdate();

                        JOptionPane.showMessageDialog(null, "Su pedido ha sido realizado, recibirá un email de confirmación en su correo electrónico.");
                        this.dispose();
                        DatosCompra datos = new DatosCompra();
                    } catch (SQLException e) {
                        e.printStackTrace();
                    }
                } else if (IniciarSesionTrabajador.esTrabajador == true || RegistrarTrabajador.bandera == true) {
                    try {
                        Connection cn = conexion.Conexion.conectar();

                        String enviarConsulta = "INSERT INTO compras (NombreComprador, ApellidosComprador, DNIComprador, CuentaBancaria, CorreoCompra, ID_TRABAJADORES, ID_VIDEOJUEGOS) VALUES (?, ?, ?, ?, ?, ?, ?) ";

                        PreparedStatement sentencia = cn.prepareStatement(enviarConsulta);

                        sentencia.setString(1, nombreComprador);
                        sentencia.setString(2, apellidosComprador);
                        sentencia.setString(3, dniComprador);
                        sentencia.setString(4, cuentaComprador);
                        sentencia.setString(5, correoCompra);
                        if (RegistrarTrabajador.bandera == true) {
                            sentencia.setInt(6, RegistrarTrabajador.ID);
                        } else {
                            sentencia.setInt(6, IniciarSesionTrabajador.ID);
                        }
                        sentencia.setInt(7, Resultados.ID);

                        sentencia.executeUpdate();

                        JOptionPane.showMessageDialog(null, "Su pedido ha sido realizado, recibirá un email de confirmación en su correo electrónico.");
                        cn.close();
                        this.dispose();
                        DatosCompra datos = new DatosCompra();

                    } catch (SQLException e) {
                        e.printStackTrace();
                    }

                }

            }
        } else {
            JOptionPane.showMessageDialog(null, "Debes aceptar la política de terminos y condiciones");
        }


    }//GEN-LAST:event_BotonFinalizarCompraActionPerformed

