    private void BotonVerDetallesPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonVerDetallesPedidoActionPerformed
        // TODO add your handling code here:

        try {
            Connection cn = conexion.Conexion.conectar();
            if (IniciarSesionCliente.cogerNombreUsu == true || RegistrarCliente.bandera == true) {
                String sql1 = "SELECT * FROM compras AS CO INNER JOIN clientes AS C ON CO.ID_CLIENTES = C.ID WHERE C.ID = ?";
                PreparedStatement enviarConsultas = cn.prepareStatement(sql1);
                if (RegistrarCliente.bandera == true) {
                    enviarConsultas.setInt(1, RegistrarCliente.ID);
                } else {
                    enviarConsultas.setInt(1, IniciarSesionCliente.ID);
                }

                ResultSet resultado = enviarConsultas.executeQuery();

                if (resultado.next()) {

                    nombresCompradores = resultado.getString("NombreComprador");
                    apellidosCompradores = resultado.getString("ApellidosComprador");
                    dniCompradores = resultado.getString("DNIComprador");
                    cuentasBancarias = resultado.getString("CuentaBancaria");
                    correosCompras = resultado.getString("CorreoCompra");
                    IDCompras = resultado.getInt("ID");
                } else {
                    JOptionPane.showMessageDialog(null, "Este cliente no ha realizado aÃºn ninguna compra");
                }

            } else if (IniciarSesionTrabajador.esTrabajador == true || RegistrarTrabajador.bandera == true) {
                String sql1 = "SELECT * FROM compras AS CO INNER JOIN trabajadores AS T ON CO.ID_TRABAJADORES = T.ID WHERE T.ID = ?";
                PreparedStatement enviarConsultas = cn.prepareStatement(sql1);
                if (RegistrarCliente.bandera == true) {
                    enviarConsultas.setInt(1, RegistrarTrabajador.ID);
                } else {
                    enviarConsultas.setInt(1, IniciarSesionTrabajador.ID);
                }

                ResultSet resultado = enviarConsultas.executeQuery();

                if (resultado.next()) {

                    nombresCompradores = resultado.getString("NombreComprador");
                    apellidosCompradores = resultado.getString("ApellidosComprador");
                    dniCompradores = resultado.getString("DNIComprador");
                    cuentasBancarias = resultado.getString("CuentaBancaria");
                    correosCompras = resultado.getString("CorreoCompra");
                    IDCompras = resultado.getInt("ID");

                } else {
                    JOptionPane.showMessageDialog(null, "Este trabajador no ha realizado aÃºn ninguna compra");
                }
            }

            String sql2 = "SELECT * FROM videojuegoSeleccionado AS V INNER JOIN compras AS CO ON V.ID = CO.ID_VIDEOJUEGOS WHERE CO.ID = ?";
            PreparedStatement enviarConsultas2 = cn.prepareStatement(sql2);
            enviarConsultas2.setInt(1, IDCompras);
            ResultSet resultado2 = enviarConsultas2.executeQuery();
            TodosDatosCompra todosDatos = new TodosDatosCompra();

            if (resultado2.next()) {
                nombreJuego = resultado2.getString("NombreJuego");
                generoJuego = resultado2.getString("GeneroJuego");
                plataformaJuego = resultado2.getString("PlataformaJuego");
                modoJuegoJuego = resultado2.getString("ModoJuego");
                precioJuego = resultado2.getString("PrecioJuego");
                fechaJuego = resultado2.getString("FechaJuego");
                urlJuego = resultado2.getString("URL");

                icono = new ImageIcon("src/images/Juegos/" + urlJuego);
                int anchoLabel = todosDatos.ValorFoto.getWidth();
                int altoLabel = todosDatos.ValorFoto.getHeight();
                Image imagenRedimensionada = icono.getImage().getScaledInstance(anchoLabel, altoLabel, Image.SCALE_SMOOTH);
                ImageIcon iconoRedimensionado = new ImageIcon(imagenRedimensionada);

                todosDatos.setVisible(true);

                todosDatos.NombreComprador.setText(nombresCompradores + " " + apellidosCompradores);
                todosDatos.DNIComprador.setText(dniCompradores);
                todosDatos.BancoComprador.setText(cuentasBancarias.substring(0, 6) + "...");
                todosDatos.CorreoComprador.setText(correosCompras);
                todosDatos.NombreVideojuego.setText(nombreJuego);
                todosDatos.PrecioJuego.setText(precioJuego);
                todosDatos.PlataformaJuego.setText(plataformaJuego);
                todosDatos.GeneroJuego.setText(generoJuego);
                todosDatos.ValorFoto.setIcon(iconoRedimensionado);
                todosDatos.AreaDescripcion.setText("El juego " + nombreJuego + "\npertenece al gÃ©nero\n" + generoJuego + " y estÃ¡\ndisponible para " + plataformaJuego);
            }

            cn.close();
        } catch (SQLException e) {
            System.err.println("Error al obtener los datos" + e.getMessage());
        }

    }//GEN-LAST:event_BotonVerDetallesPedidoActionPerformed

