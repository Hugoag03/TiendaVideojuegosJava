    private void BotonComprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonComprobarActionPerformed
        // TODO add your handling code here:
        String patronDNI = "\\d{8}[A-HJ-NP-TV-Z]";
        String patronNoNums = "[a-zA-ZáéíóúÁÉÍÓÚñÑüÜ\\s]+";
        String patronCuentaIBAN = "^([A-Z]{2})(\\d{18})$";
        String patronEmail = "^.+@.+\\..+$";

        if (NombreCompra.getText().isEmpty() || DNICompra.getText().isEmpty() || ApellidosCompra.getText().isEmpty() || BancoCompra.getText().isEmpty() || CorreoCompra.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debes rellenar todos los campos", "Rellena", JOptionPane.WARNING_MESSAGE);
        } else if (!DNICompra.getText().matches(patronDNI)) {
            JOptionPane.showMessageDialog(null, "Debes introducir un DNI válido");
        } else if (!NombreCompra.getText().matches(patronNoNums)) {
            JOptionPane.showMessageDialog(null, "Debes introducir un nombre sin números");
        } else if (!ApellidosCompra.getText().matches(patronNoNums)) {
            JOptionPane.showMessageDialog(null, "Debes introducir apellidos sin números");
        } else if (!BancoCompra.getText().matches(patronCuentaIBAN)) {
            JOptionPane.showMessageDialog(null, "Debes introducir un IBAN válido");
        } else if (!CorreoCompra.getText().matches(patronEmail)) {
            JOptionPane.showMessageDialog(null, "Debes introducir un código postal válido");
        } else {
            JOptionPane.showMessageDialog(null, "Los datos son válidos");
            CheckImp.setEnabled(true);
            Check2.setEnabled(true);
        }
    }//GEN-LAST:event_BotonComprobarActionPerformed

